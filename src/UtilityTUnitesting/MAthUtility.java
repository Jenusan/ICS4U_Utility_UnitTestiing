package UtilityTUnitesting;

public class MAthUtility {
    /**
     * adds
     * @param intNumber1
     * @param intNumber2
     * @return
     */
    public static int add(int intNumber1, int intNumber2){
        return intNumber1 + intNumber2;
    }

    /**
     * subtracts
     * @param intNumber1 
     * @param intNumber2
     * @return
     */
    public static int sub(int intNumber1, int intNumber2){
        return intNumber1 - intNumber2;
    }


}
